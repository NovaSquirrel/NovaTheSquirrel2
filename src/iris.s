.segment "IrisEffect"
.a16
.i16
.export IrisInitTable
.proc IrisInitTable
  php
  phb
  seta8
  ; To hide the window, use a negative width
  lda #0
  sta f:IrisEffectBuffer1
  sta f:IrisEffectBuffer2
  plb
  plp
  rtl
.endproc

.a16
.i16
.export IrisInitHDMA
.proc IrisInitHDMA
  php
  seta16
  lda #(<WINDOW1L << 8) | DMA_01
  sta DMAMODE+$70
  ; Use the buffer that was prepared last frame
  lda framecount
  lsr
  bcc :+
    lda #.loword(IrisEffectBuffer1)
    sta DMAADDR+$70
    bra :++
  :
    lda #.loword(IrisEffectBuffer2)
    sta DMAADDR+$70
  :

  seta8
  lda #^IrisEffectBuffer1
  sta DMAADDRBANK+$70

  lda #%00100000
  sta WOBJSEL
  lda #%01010000
  sta CGWSEL

  lda #$80
  tsb HDMASTART_Mirror
  plp
  rtl
.endproc

.a16
.i16
.export IrisDisable
.proc IrisDisable
  php
  seta8
  ; Disable the windows now
  lda HDMASTART_Mirror
  and #$7F
  sta HDMASTART_Mirror
  sta HDMASTART

  lda #%00000000
  sta CGWSEL
  plp
  rtl
.endproc

