.i16
; Updates the iris effect's tables
; A = Frame number
; 0 = Center X coordinate
; 1 = Center Y coordinate
.export IrisUpdate
.proc IrisUpdate
OriginX = 0
OriginY = 1
Frame = 2
RowsLeft = 3
Length = 4
TopLength = 5
BottomLength = 7
  php
  seta8
  sta Frame

  tdc ; Clear all of accumulator for TAX/TAY

  lda Frame
  sta CPUMCAND
  lda #224
  sta CPUMUL

  ; Choose a buffer
  ldx #0
  lda framecount
  lsr
  bcs :+
    ldx #224*3+5
  :

  ; Be able to use absolute,y on the table
  phb
  lda #^IrisTable
  pha
  plb

  lda Frame ; B is zero still from the earlier TDC
  tay
  lda IrisLenTable,y
  sta Length

  ; Empty space at the top, if needed
  lda OriginY
  sub Length
  beq NoEmptySpaceAtTop
  bcc NoEmptySpaceAtTop
    ; Do it with one entry
    cmp #128
    bcs @Two
    ; Do it with one entry
    sta f:IrisEffectBuffer1,x
    inx
    lda #255
    sta f:IrisEffectBuffer1,x
    inx
    ina ; A = 0
    sta f:IrisEffectBuffer1,x
    inx
    bra NoEmptySpaceAtTop
  @Two:
    ; Do it with two entries
    sub #127
    sta f:IrisEffectBuffer1,x
    inx
    lda #255
    sta f:IrisEffectBuffer1,x
    sta f:IrisEffectBuffer1+3,x
    inx
    ina ; A = 0
    sta f:IrisEffectBuffer1,x
    sta f:IrisEffectBuffer1+3,x
    inx
    ;---
    lda #127
    sta f:IrisEffectBuffer1,x
    inx
    inx
    inx
  NoEmptySpaceAtTop:

  ; Top of the circle
  lda Length
  cmp OriginY
  bcc :+
LimitTop:
    lda OriginY
  :
  sta TopLength
  stz TopLength+1

  ; Bottom of the circle
  lda OriginY
  add Length
  bcs LimitBottom ; < 256
  cmp #224
  bcc :+ ; < 224
LimitBottom:
    lda #224
  :
  sub OriginY
  sta BottomLength

  ; Do the top and bottom half of the circle
  seta16
  lda CPUPROD
  add TopLength
  tay
  seta8
  lda TopLength
  beq :+
    sta RowsLeft
    jsr BuildLoopTop
  :

  ldy CPUPROD
  lda BottomLength
  beq :+
    sta RowsLeft
    jsr BuildLoopBottom
  :

  ; Turn off the window at the bottom
  lda #1
  sta f:IrisEffectBuffer1+0,x
  lda #255
  sta f:IrisEffectBuffer1+1,x
  lda #0
  sta f:IrisEffectBuffer1+2,x
  lda #0
  sta f:IrisEffectBuffer1+3,x

  plb
  plp
  rtl

BuildLoopTop:
  lda #1
  sta f:IrisEffectBuffer1,x
  inx

  lda OriginX
  sub IrisTable,y
  bcs :+
    lda #0 ; Clear accumulator
  :
  sta f:IrisEffectBuffer1,x
  inx

  lda OriginX
  add IrisTable,y
  bcc :+
    lda #255
  :
  sta f:IrisEffectBuffer1,x
  inx

  dey ; Next row
  dec RowsLeft
  bne BuildLoopTop
  rts

BuildLoopBottom:
  lda #1
  sta f:IrisEffectBuffer1,x
  inx

  lda OriginX
  sub IrisTable,y
  bcs :+
    lda #0 ; Clear accumulator
  :
  sta f:IrisEffectBuffer1,x
  inx

  lda OriginX
  add IrisTable,y
  bcc :+
    lda #255
  :
  sta f:IrisEffectBuffer1,x
  inx

  iny ; Next row
  dec RowsLeft
  bne BuildLoopBottom
  rts
.endproc

